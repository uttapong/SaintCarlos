<p>
Bioplastique Implants are solid, irregularly textured, vulcanized medical grade polydimethylsiloxane particles suspended in a carrier gel of polyvinylpyrrolidone (also known as PVP or povidone). The carrier is removed by the reticuloendothelial systems and excreted unchanged from the body via renal excretion.
 <br><br>
Bioplastique was developed in 1987 and has gained wide use throughout the world. It has been proven to be useful in many clinical applications with few complications.  
 <br><br>
Bioplastique Implants are indicated for subdermal administration in the augmentation and restoration of soft tissue defects and contour deficiences. The best results are achieved when the implants are used in the malar region (including the zygomatic arch), nasal bridge deformities, the nasal tip, the chin, and the mandibular lining. Bioplastique Implants are also indicated for use in vocal cord medialization. 
 <br><br>
Injection of Bioplastique suspension is accomplished using an administration gun incorporating a ratchet mechanism specifically designed for use with Bioplastique syringes. 
 <br><br>
The use of this product induces lower surgical risk, shorter recovery time and less expense than more invasive alternatives. The main attributes of Bioplastique Implants include the following:
 <br> <br>
&nbsp;&nbsp;&nbsp;&nbsp;-	Bioplastique Implants are designed to offer surgeons and their patients a minimally invasive, long-lasting and cost-effective treatment
 <br> <br>
&nbsp;&nbsp;&nbsp;&nbsp;-	The raw materials are all Pharmaceutical or Medical Grade. All components used in Bioplastique Implants are commonly used in many other biomedical applications and have a well-documented safety record for biomedical use.
 <br> <br>
&nbsp;&nbsp;&nbsp;&nbsp;-	The surface of the material is heavily textured to provide a multi-planar host tissue/implant interface for rapid deposition of collage around and through the individual implants and around the periphery of the injected product. The implants also have a highly irregular shape which minimizes the potential for migration by allowing individual implants to interlock with each other for form larger agglomerates.
 <br> <br>
&nbsp;&nbsp;&nbsp;&nbsp;-	Clinical experience with patients since 1989 indicates no serious product-related adverse reactions of any kind.
 <br> <br>
&nbsp;&nbsp;&nbsp;&nbsp;-	Implantation of Bioplastique Implants is technologically feasible, easily performed and effective. Laboratory evaluations, biological safety testing and clinical evaluations all demonstrate the safety and efficacy of Bioplastique Implants

</p>



