<p>An injectable implant based on hyaluronic acid and acrylic hydrogel, is of non-animal origin and has a long lasting result, around 5-7 years at the most.
<br/><br/>
DermaDeep® is intended for cheek augmentation, chin augmentation, deep nasolabial folds, scar correction, remodeling and correction of nose, following a rhinoplasty.
<br/><br/>
DermaDeep® contains hyaluronic acid (60%), a glycoaminoglycane found in its natural form both in connective tissue as well as in the skin where it ensures their water supply. Due to its high viscoelastic and fluid properties, hyaluronic acid is an ideal injection vector. The hyaluronic acid is cross-linked in order to slow down its cutaneous resorbtion and therefore lengthen its effectiveness.
<br/><br/>
The other component is acrylic hydrogel (40%), a member of the acrylate family, has a 26% water content which makes it better accepted by tissues, gives it flexibility and elasticity and ensures its integration into the surrounding tissue and adaptation to deformities of the dermis caused by movement of facial muscles. 
<br/><br/>
A three-month period is recommended between two sessions to avoid risks of excessive fibrosis. A medical doctor with extensive training on the injection technique for filling wrinkles should administer this procedure.
