<h4>Hydro-Colon</h4>
<p>คือวิธีการสวนล้างลำไส้ที่นุ่มนวล ปลอดภัยและได้ผลในการใช้น้ำบริสุทธิ์ช่วยเข้าไปนำพาเอาของเสียต่างๆและสารพิษที่ตกค้างอยู่ในลำไส้ใหญ่โดยไม่ใช้สารเคมีใดๆทั้งสิ้นจะช่วยในการล้างของเสียและสารพิษที่สะสมมาเป็นเวลานานในลำไส้ใหญ่ และเมื่อลำไส้ใหญ่ของคุณอยู่ในสภาวะที่สะอาดสมดุล แบคทีเรียที่ดีในลำไส้จะสามารถอยู่อาศัยและเจริญเติบโตเพื่อสร้างวิตามินและภูมิคุ้มกันให้กับร่างกายได้ นอกจากนี้น้ำบริสุทธิ์ที่ร่างกายดูดซึมเข้าไปจะช่วยเพิ่มความชุ่มชื้นให้ผิวพรรณทั่วร่างกายของคุณ โดยรวมแล้วการทำ Hydro colon จะช่วยในการฟื้นฟูร่างกายที่สะอาด สมดุล ชุ่มชื้น และแข็งแรงให้กับคุณ และขั้นตอนนี้ปลอดภัย เนื่องจากเราจะมีแพทย์และพนักงานดูแลท่านอย่างใกล้ชิดในสถานที่ที่มีความเป็นส่วนตัว</p>   

<h4>Oxygen Therapy</h4>
<p>“เมื่อใดก็ตามที่ท่านรู้สึกว่าหมดพลังงาน นั่นหมายความว่าร่างกายของท่านขาดออกซิเจน” เนื่องจากออกซิเจนเป็นแหล่งพลังงานของชีวิต การบำบัดด้วยออกซิเจนที่บริสุทธิ์สามารถช่วยให้ระบบของเซลล์นำพลังงานที่ได้ไปเผาผลาญไขมัน ช่วยให้รูปร่างดี และ คงความอ่อนเยาว์ นอกจากนี้ออกซิเจนยังนำมาซึ่งพลังงานที่สามารถกระจายไปยังส่วนต่างๆของร่างกาย ทั้งระบบกล้ามเนื้อ , ทำให้อัตราการเต้นของหัวใจลดลงและคงที่,สามารถลดความดันโลหิตให้อยู่ในระดับที่ปรกติ,กระตุ้นให้ร่างกายสามารถต่อสู้กับเชื้อโรค,ทำให้อารมณ์ดีขึ้นและช่วยแก้ปัญหาการนอนหลับในคนที่นอนไม่หลับ จะพบว่าพลังของออกซิเจนบริสุทธิ์มีอยู่มากมาก เพราะร่างกายเราใช้ออกซิเจนเป็นแหล่งของพลังงานชีวิตนั่นเอง</p>

