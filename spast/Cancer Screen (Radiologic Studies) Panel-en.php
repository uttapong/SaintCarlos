	<h3>Cancer Screen (Radiologic Studies) Panel</h3>
	 <h4>Radiologic studies use to screen malignancy are cheaper and non-invasive compared to endoscopic studies. However, its sensitivity in detecting early stages of cancer is not that wellestablished unlike endoscopy.</h4><br>

<ul class="bullet3">
<li>CT Scan Brain</li>
<li>Ultrasound Whole Abdomen</li>
<li>Upper GI Study</li>

<ul class="bullet2"><li>It is series of x-rays of the esophagus and stomach (the upper gastrointestinal or GI tract. Prior to taking the x-rays, the patient is first asked to drink a barium solution which a thick, chalky liquid that is why it is also called a barium swallow. The barium will outline the stomach on the x-rays, helping the doctor find tumors or other abnormal areas. During the test, the doctor may pump air into the stomach to make small tumors easier to see.</li>
</ul></ul><br>

<ul class="bullet3">
<li>Lower GI Study</li>
<ul class="bullet2"><li>It is a series of x-rays of the colon and rectum. The patient is given an enema with a barium solution, and air is pumped into the rectum. The barium and air outline the colon and rectum on the x-rays. Polyps and large masses or colonic obstruction may show up on the x-ray.</li>
</ul></ul><br>



